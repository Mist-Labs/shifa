interface I18nContextValue {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => Promise<void>;
  t: (key: string) => string;
}

const I18nContext = createContext<I18nContextValue>({
  language: DEFAULT_LANGUAGE,
  setLanguage: async () => {},
  t: (key) => translations[DEFAULT_LANGUAGE][key] ?? key,
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>(DEFAULT_LANGUAGE);

  useEffect(() => {
    let active = true;
    getLanguagePreference().then((nextLanguage) => {
      if (active) setLanguageState(nextLanguage);
    });
    return () => {
      active = false;
    };
  }, []);

  const setLanguage = useCallback(async (nextLanguage: AppLanguage) => {
    setLanguageState(nextLanguage);
    await saveLanguagePreference(nextLanguage);
  }, []);

  const t = useCallback((key: string) => translations[language][key] ?? translations.en[key] ?? key, [language]);
  const value = useMemo(() => ({ language, setLanguage, t }), [language, setLanguage, t]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}

export async function getLanguagePreference(): Promise<AppLanguage> {
  const rows = await selectRows<{ value: string }>(`SELECT value FROM app_settings WHERE key = ? LIMIT 1`, ['language']);
  return toAppLanguage(rows[0]?.value);
}

export async function saveLanguagePreference(language: string): Promise<void> {
  await executeSql(`INSERT OR REPLACE INTO app_settings (key, value, updated_at) VALUES (?, ?, ?)`, [
    'language',
    toAppLanguage(language),
    Date.now(),
  ]);
}

export function toAppLanguage(value?: string): AppLanguage {
  if (value === 'ar' || value === 'so' || value === 'fr' || value === 'ln' || value === 'rw' || value === 'ha' || value === 'en') return value;
  return DEFAULT_LANGUAGE;
}
